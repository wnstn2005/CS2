package class2;

public class Number {
    public static void main(String[] args) {

        // 정수

        int a = 2;
        int age = 20;


        byte b;
        b = 127;

        short c = 32767;


        // byte, short, int, long - 정수형
        // byte = -128 < 127 표현 - X
        // short = -32768 < 32767 =65535 - X
        // int  = -2,147,483,648 < 2,147,483,647
        // long = –9,223,372,036,854,775,808 < 9,223,372,036,854,775,807


        // 실수


        float d = 3.14f;
        double e = 3.145195165168161651651651869161;

        // float < double


        // 8진수 - 0을 붙이면 8진수로 표현 가능

        int eight = 023;

        // 16진수 - 0x로 붙이면 16진수로 표현 가능

        int sixteen = 0x2;


    }
}
