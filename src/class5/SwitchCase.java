package class5;

public class SwitchCase {


    public static void main(String[] args) {

        int age = 11;

        switch (age) {
            case 11:
                System.out.println("11입니다.");
                break;
            case 12:
                System.out.println("12입니다.");
                break;
            case 13:
                System.out.println("13입니다.");
                break;
            case 14:
                System.out.println("14입니다.");
                break;
            case 15:
                System.out.println("15입니다.");
                break;
        }


    }


}


